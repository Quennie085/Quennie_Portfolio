// Drag and drop functionality for About section
const aboutSection = document.querySelector('#about');
let offsetX, offsetY;

aboutSection.addEventListener('dragstart', (e) => {
    e.dataTransfer.setData('text/plain', aboutSection.id);
    offsetX = e.clientX - aboutSection.getBoundingClientRect().left;
    offsetY = e.clientY - aboutSection.getBoundingClientRect().top;
    aboutSection.style.opacity = '0.7';
});

document.addEventListener('dragover', (e) => {
    e.preventDefault();
});

document.addEventListener('drop', (e) => {
    e.preventDefault();
    aboutSection.style.position = 'relative';
    aboutSection.style.left = 'auto';
    aboutSection.style.top = 'auto';
    aboutSection.style.opacity = '1';
});

// Make about section draggable
aboutSection.style.cursor = 'move';
aboutSection.style.position = 'relative';
